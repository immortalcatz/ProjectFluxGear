package oldcode.projectfluxgear.util.block.subtile;

   /*
	*   Subtiles: An awesome way of having dynamic TileEntities!
	*
	*   Invented by Vazkii, Subtiles are a way of having one TileEntity with sub-TileEntities in the same metadata slot,
	*   through use of NBT, or Named Binary Tags.
	*
	*   I use this so the hitbox will be determined by metadata, and so I don't have to worry about putting things in a
	*   logical order metadata-wise.
	*
	*   Again, this is Vazkii's creation, not mine.
	*/